@Configuration
public class GatewayConfig {

    /**
     * 服务名前缀（lb = load balance 负载均衡）
     */
    private static final String PREFIX = "lb://";

    @Bean
    public RouteLocator routes(RouteLocatorBuilder builder) {
        return builder.routes()
                .route(r ->
                    r.path("/user1/**").filters(f -> f.stripPrefix(1)).uri(PREFIX + "user").id("user")
                )
                .route(r -> r.path("/oauth/**").uri(PREFIX + "auth-server").id("auth-server"))
                .build();
    }

    /**
     * 设置跨域过滤器
     */
    @Profile("dev")
    @Bean
    public WebFilter corsFilter() {

        final String ALLOWED_HEADERS = "*";
        final String ALLOWED_METHODS = "*";
        final String ALLOWED_ORIGIN = "*";
        final String ALLOWED_Expose = "*";
        final String MAX_AGE = "18000L";

        return (ctx, chain) -> {

            ServerHttpRequest request = ctx.getRequest();
            if (CorsUtils.isCorsRequest(request)) {
                ServerHttpResponse response = ctx.getResponse();
                HttpHeaders headers = response.getHeaders();
                headers.add("Access-Control-Allow-Origin", ALLOWED_ORIGIN);
                headers.add("Access-Control-Allow-Methods", ALLOWED_METHODS);
                headers.add("Access-Control-Max-Age", MAX_AGE);
                headers.add("Access-Control-Allow-Headers", ALLOWED_HEADERS);
                headers.add("Access-Control-Expose-Headers", ALLOWED_Expose);
                headers.add("Access-Control-Allow-Credentials", "true");
                if (request.getMethod() == HttpMethod.OPTIONS) {
                    response.setStatusCode(HttpStatus.OK);
                    return Mono.empty();
                }
            }

            return chain.filter(ctx);
        };
    }
}
